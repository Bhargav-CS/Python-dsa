class Solution:
    def celebrity(self, mat):
        n = len(mat)
        if n == 1:
            return 0
        
        for row in mat:
            if sum(row) == 0:
                idx = mat.index(row)
            
                for i in range(n):
                    if mat[i][idx] != 1 and i != idx:
                        return -1
            
                return idx
        
        return -1              
       
    
